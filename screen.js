// ═══════════════════════════════════════════════════════════════════════
// Script loader
// ═══════════════════════════════════════════════════════════════════════

function _loadScript(url) {
    return new Promise((resolve, reject) => {
        if (document.querySelector(`script[src="${url}"]`)) { resolve(); return; }
        const s = document.createElement('script');
        s.src = url;
        s.onload = resolve;
        s.onerror = () => reject(new Error('Failed to load ' + url));
        document.head.appendChild(s);
    });
}

// ═══════════════════════════════════════════════════════════════════════
// WebAudioFont synthesizer
// ═══════════════════════════════════════════════════════════════════════

async function _synthInit() {
    if (_synthPlayer) return;
    try {
        if (!_playerScriptLoaded) {
            await _loadScript(WAF_PLAYER_URL);
            _playerScriptLoaded = true;
        }
        if (typeof WebAudioFontPlayer === 'undefined') return;

        _audioCtx = new (window.AudioContext || window.webkitAudioContext)();
        _synthGain = _audioCtx.createGain();
        _synthGain.gain.value = _cfg.synthVolume;
        _synthGain.connect(_audioCtx.destination);
        _synthPlayer = new WebAudioFontPlayer();

        await _synthLoadInstrument(_cfg.instrumentIdx);
    } catch (e) {
        console.warn('[Piano] Synth init failed:', e);
    }
}

async function _synthLoadInstrument(idx) {
    const inst = INSTRUMENTS[idx];
    if (!inst || !_synthPlayer || !_audioCtx) return;
    _synthLoading = true;
    const varName = _wafVar(inst.gm);

    try {
        if (!window[varName]) {
            await _loadScript(_wafUrl(inst.gm));
        }
        const preset = window[varName];
        if (preset) {
            _synthPlayer.adjustPreset(_audioCtx, preset);
            _synthPreset = preset;
        }
    } catch (e) {
        console.warn('[Piano] Failed to load instrument:', inst.name, e);
    }
    _synthLoading = false;
}

function _synthEnsureCtx() {
    if (_audioCtx && _audioCtx.state === 'suspended') {
        _audioCtx.resume();
    }
}

function _synthNoteOn(midi, velocity) {
    if (!_synthPlayer || !_synthPreset || !_audioCtx || !_synthGain) return;
    _synthEnsureCtx();

    const existing = _noteEnvelopes.get(midi);
    if (existing) { try { existing.cancel(); } catch (_) {} }

    const vol = (velocity / 127) * _cfg.synthVolume;
    const envelope = _synthPlayer.queueWaveTable(
        _audioCtx, _synthGain, _synthPreset, 0, midi, 999, vol
    );
    _noteEnvelopes.set(midi, envelope);
}

function _synthNoteOff(midi) {
    const env = _noteEnvelopes.get(midi);
    if (env) {
        try { env.cancel(); } catch (_) {}
        _noteEnvelopes.delete(midi);
    }
}

function _synthReleaseAll() {
    for (const env of _noteEnvelopes.values()) {
        try { env.cancel(); } catch (_) {}
    }
    _noteEnvelopes.clear();
}

function _synthSetVolume(vol) {
    _saveCfg('synthVolume', vol);
    if (_synthGain) _synthGain.gain.value = vol;
}

// ═══════════════════════════════════════════════════════════════════════
// Web MIDI input
// ═══════════════════════════════════════════════════════════════════════

async function _midiInit() {
    if (_midiAccess) return;
    if (!navigator.requestMIDIAccess) return;
    try {
        _midiAccess = await navigator.requestMIDIAccess({ sysex: false });
        _midiAccess.onstatechange = () => _midiUpdateDeviceList();
        _midiAutoConnect();
    } catch (e) {
        console.warn('[Piano] MIDI access denied:', e);
    }
}

function _midiAutoConnect() {
    if (!_midiAccess) return;
    const inputs = [];
    _midiAccess.inputs.forEach(inp => inputs.push(inp));
    if (!inputs.length) return;

    // Distinguish "never picked a device" from "explicitly picked
    // None". localStorage.getItem returns null for the never-set
    // case and '' for an explicit-None save (via _midiConnect).
    // Read raw rather than _cfg.midiInputId, which collapses both
    // to '' via its `|| ''` default in the config initialiser.
    let raw = null;
    try { raw = localStorage.getItem(STORE_KEYS.midiInputId); } catch (_) {}
    if (raw === '') return;  // user opted out — respect it

    const target = inputs.find(i => i.id === raw) || inputs[0];
    _midiConnect(target.id);
}

function _midiConnect(id) {
    if (_midiInput) _midiInput.onmidimessage = null;
    _midiInput = null;

    // Persist the choice regardless of match. An empty id is the
    // "None" option and must be saved so _midiAutoConnect() on the
    // next init respects the opt-out instead of auto-picking
    // inputs[0] again. A non-empty id that doesn't match any
    // attached device also gets saved here — harmless, and lets a
    // re-plugged device reconnect to its saved slot without extra
    // UI work.
    _saveCfg('midiInputId', id || '');

    if (!id || !_midiAccess) {
        _midiUpdateDeviceList();
        return;
    }
    _midiAccess.inputs.forEach(inp => {
        if (inp.id === id) {
            _midiInput = inp;
            // Wire the handler only when the renderer is active.
            // If _midiInit's async chain resolves after destroy(),
            // we still CAPTURE the device so the next init's
            // _midiResumeHandler can re-wire without a second
            // _midiAutoConnect — we just don't listen right now.
            if (_midiActive) _midiInput.onmidimessage = _midiOnMessage;
        }
    });
    _midiUpdateDeviceList();
}

function _midiPauseHandler() {
    // Called from destroy() — detach the message handler so the
    // connected device stops firing _onNoteOn / _onNoteOff into a
    // plugin no longer visible. Keep the _midiInput reference so a
    // future init() can reattach without the user having to re-pick
    // their device in settings. Flipping _midiActive BEFORE the
    // detach also prevents a late-resolving _midiConnect (from an
    // in-flight _midiInit started in the most recent init()) from
    // re-wiring the handler on an already-destroyed renderer.
    _midiActive = false;
    if (_midiInput) _midiInput.onmidimessage = null;
}

function _midiResumeHandler() {
    // Called from init() — restore the handler if MIDI was already
    // connected in a previous piano lifetime. If _midiInput is null
    // this is a no-op; _midiInit + _midiAutoConnect will connect
    // afresh from settings. Flip _midiActive first so any in-flight
    // _midiConnect completing shortly after this returns wires the
    // handler too.
    _midiActive = true;
    if (_midiInput) _midiInput.onmidimessage = _midiOnMessage;
}

function _midiOnMessage(e) {
    const [status, note, velocity] = e.data;
    const ch = status & 0x0F;
    if (_cfg.midiChannel >= 0 && ch !== _cfg.midiChannel) return;

    const cmd = status & 0xF0;
    const transposed = note + _cfg.transpose;

    if (cmd === 0x90 && velocity > 0) {
        _onNoteOn(transposed, velocity);
    } else if (cmd === 0x80 || (cmd === 0x90 && velocity === 0)) {
        _onNoteOff(transposed);
    } else if (cmd === 0xB0 && note === 64) {
        if (velocity >= 64) {
            _sustainOn = true;
        } else {
            _sustainOn = false;
            for (const midi of _sustainedNotes) {
                _heldNotes.delete(midi);
                _synthNoteOff(midi);
            }
            _sustainedNotes.clear();
        }
    }
}

function _onNoteOn(midi, velocity) {
    if (midi < 0 || midi > 127) return;
    _heldNotes.set(midi, velocity);
    _synthNoteOn(midi, velocity);
    _synthEnsureCtx();
    if (_cfg.hitDetection) _checkHit(midi);
}

function _onNoteOff(midi) {
    if (midi < 0 || midi > 127) return;
    if (_sustainOn) {
        _sustainedNotes.add(midi);
        return;
    }
    _heldNotes.delete(midi);
    _synthNoteOff(midi);
}

function _midiUpdateDeviceList() {
    const sel = document.getElementById('piano-midi-select');
    if (!sel || !_midiAccess) return;

    const inputs = [];
    _midiAccess.inputs.forEach(inp => inputs.push(inp));

    // Build <option> elements via the DOM API rather than concatenating
    // an HTML string. MIDI device names come from the attached
    // hardware and can contain characters that would otherwise inject
    // markup (a maliciously-named device, or even an accidental "<"
    // in the vendor string) directly into the settings panel. Setting
    // .value and .textContent escapes both fields safely.
    sel.textContent = '';
    const noneOpt = document.createElement('option');
    noneOpt.value = '';
    noneOpt.textContent = 'None';
    sel.appendChild(noneOpt);
    for (const inp of inputs) {
        const opt = document.createElement('option');
        opt.value = inp.id;
        opt.textContent = inp.name;
        if (_midiInput && _midiInput.id === inp.id) opt.selected = true;
        sel.appendChild(opt);
    }
}

// ═══════════════════════════════════════════════════════════════════════
// Hit detection / accuracy scoring
// ═══════════════════════════════════════════════════════════════════════
//
// Score against the filtered chart the user is seeing — not the full
// unfiltered arrays — so the difficulty slider applies to hit detection
// too. draw() caches bundle.notes/chords/currentTime into _latest*
// every frame; async MIDI events read those snapshots.

function _checkHit(playedMidi) {
    const t = _latestTime;
    const notes = _latestNotes;
    const chords = _latestChords;

    let foundHit = false;

    if (notes) {
        for (const n of notes) {
            if (n.t > t + HIT_TOLERANCE + 0.5) break;
            if (n.t < t - HIT_TOLERANCE - 0.5) continue;
            const songMidi = noteToMidi(n.s, n.f);
            const key = _noteKey(n.t, songMidi);
            if (songMidi === playedMidi && Math.abs(n.t - t) <= HIT_TOLERANCE && !_hitNoteKeys.has(key)) {
                _hitNoteKeys.add(key);
                foundHit = true;
                break;
            }
        }
    }

    if (!foundHit && chords) {
        for (const c of chords) {
            if (c.t > t + HIT_TOLERANCE + 0.5) break;
            if (c.t < t - HIT_TOLERANCE - 0.5) continue;
            for (const cn of (c.notes || [])) {
                const songMidi = noteToMidi(cn.s, cn.f);
                const key = _noteKey(c.t, songMidi);
                if (songMidi === playedMidi && Math.abs(c.t - t) <= HIT_TOLERANCE && !_hitNoteKeys.has(key)) {
                    _hitNoteKeys.add(key);
                    foundHit = true;
                    break;
                }
            }
            if (foundHit) break;
        }
    }

    if (foundHit) {
        _hits++;
        _streak++;
        if (_streak > _bestStreak) _bestStreak = _streak;
    } else {
        _misses++;
        _streak = 0;
        _wrongFlashes.push({ midi: playedMidi, wall: performance.now() });
    }
}

function _updateMissedNotes(t, notes, chords) {
    if (!_cfg.hitDetection) return;
    const cutoff = t - HIT_TOLERANCE - 0.05;

    if (notes) {
        for (const n of notes) {
            if (n.t > cutoff) break;
            if (n.t < cutoff - 2) continue;
            const songMidi = noteToMidi(n.s, n.f);
            const key = _noteKey(n.t, songMidi);
            if (!_hitNoteKeys.has(key) && !_missedNoteKeys.has(key) && n.t < cutoff) {
                _missedNoteKeys.add(key);
            }
        }
    }
    if (chords) {
        for (const c of chords) {
            if (c.t > cutoff) break;
            if (c.t < cutoff - 2) continue;
            for (const cn of (c.notes || [])) {
                const songMidi = noteToMidi(cn.s, cn.f);
                const key = _noteKey(c.t, songMidi);
                if (!_hitNoteKeys.has(key) && !_missedNoteKeys.has(key) && c.t < cutoff) {
                    _missedNoteKeys.add(key);
                }
            }
        }
    }

    const now = performance.now();
    while (_wrongFlashes.length && now - _wrongFlashes[0].wall > 400) {
        _wrongFlashes.shift();
    }
}

function _resetScoring() {
    _hits = 0; _misses = 0; _streak = 0; _bestStreak = 0;
    _hitNoteKeys.clear();
    _missedNoteKeys.clear();
    _wrongFlashes.length = 0;
}

function _primeLatestSnapshot() {
    // Fill _latest* from highway's public getters so a MIDI note-on
    // that lands before the first draw() of a new chart still has a
    // snapshot to score against. These getters return the unfiltered
    // chart (difficulty slider applies to bundle.notes only), so the
    // first frame of scoring uses the full chart; subsequent frames
    // switch to filter-aware data. Worst case: one key-down immediately
    // after song:ready might score against a note the difficulty slider
    // has hidden — acceptable vs. guaranteeing the user a miss.
    try {
        if (typeof highway !== 'undefined') {
            _latestNotes = typeof highway.getNotes === 'function' ? highway.getNotes() : null;
            _latestChords = typeof highway.getChords === 'function' ? highway.getChords() : null;
            _latestTime = typeof highway.getTime === 'function' ? highway.getTime() : 0;
        }
    } catch (_) {
        _latestNotes = null;
        _latestChords = null;
        _latestTime = 0;
    }
}

// ═══════════════════════════════════════════════════════════════════════
// Note range detection
// ═══════════════════════════════════════════════════════════════════════

function detectRange(notes, chords) {
    let lo = 127, hi = 0;
    if (notes) {
        for (const n of notes) {
            const m = noteToMidi(n.s, n.f);
            if (m < lo) lo = m;
            if (m > hi) hi = m;
        }
    }
    if (chords) {
        for (const c of chords) {
            for (const cn of (c.notes || [])) {
                const m = noteToMidi(cn.s, cn.f);
                if (m < lo) lo = m;
                if (m > hi) hi = m;
            }
        }
    }
    if (lo > hi) { lo = 48; hi = 84; }
    lo = Math.max(0, Math.floor(lo / 12) * 12);
    hi = Math.min(127, Math.ceil((hi + 1) / 12) * 12 - 1);
    while (hi - lo < 47) { hi = Math.min(127, hi + 12); if (hi - lo < 47 && lo > 0) lo -= 12; }
    return { lo, hi };
}

function _visibleMidiRange(notes, chords, t) {
    let lo = 127, hi = 0;
    const tMax = t + VISIBLE_SECONDS;

    if (notes) {
        for (const n of notes) {
            const end = n.t + (n.sus || 0);
            if (end < t - 0.1) continue;
            if (n.t > tMax) break;
            const m = noteToMidi(n.s, n.f);
            if (m < lo) lo = m;
            if (m > hi) hi = m;
        }
    }
    if (chords) {
        for (const c of chords) {
            if (c.t < t - 0.1) continue;
            if (c.t > tMax) break;
            for (const cn of (c.notes || [])) {
                const m = noteToMidi(cn.s, cn.f);
                if (m < lo) lo = m;
                if (m > hi) hi = m;
            }
        }
    }
    return lo <= hi ? { lo, hi } : null;
}

function _updateDisplayRange(notes, chords, t) {
    const raw = _visibleMidiRange(notes, chords, t);
    if (!raw) {
        if (_displayLo !== null) return;
        const full = detectRange(notes, chords);
        _displayLo = full.lo;
        _displayHi = full.hi;
        return;
    }

    if (_displayLo !== null && raw.lo >= _displayLo && raw.hi <= _displayHi) {
        const loSlack = raw.lo - _displayLo;
        const hiSlack = _displayHi - raw.hi;
        if (loSlack < 12 && hiSlack < 12) return;
    }

    let lo = Math.max(0, raw.lo - 2);
    let hi = Math.min(127, raw.hi + 2);
    lo = Math.floor(lo / 12) * 12;
    hi = Math.ceil((hi + 1) / 12) * 12 - 1;
    while (hi - lo < 47) {
        if (lo > 0) lo -= 12; else hi = Math.min(127, hi + 12);
    }

    _displayLo = lo;
    _displayHi = hi;
}

