// ── Persisted settings ───────────────────────────────────────────────
//
// Explicit map from the in-memory property name to the localStorage
// key. The previous code read from one set of keys (e.g.
// 'piano_midi_input') and wrote to a DIFFERENT set generated by
// snake-casing the JS name ('piano_midi_input_id'), so settings
// silently evaporated on reload — the save succeeded at the wrong
// key and the next load's getItem came back empty. Centralising the
// mapping here keeps read and write symmetric.

const STORE_KEYS = {
    midiInputId:   'piano_midi_input',
    instrumentIdx: 'piano_instrument',
    synthVolume:   'piano_synth_vol',
    midiChannel:   'piano_midi_ch',
    transpose:     'piano_transpose',
    showNoteNames: 'piano_note_names',
    hitDetection:  'piano_hit_detect',
};

const _cfg = {
    midiInputId:   localStorage.getItem(STORE_KEYS.midiInputId) || '',
    instrumentIdx: parseInt(localStorage.getItem(STORE_KEYS.instrumentIdx) || '0'),
    synthVolume:   parseFloat(localStorage.getItem(STORE_KEYS.synthVolume) || '0.7'),
    midiChannel:   parseInt(localStorage.getItem(STORE_KEYS.midiChannel) || '-1'),
    transpose:     parseInt(localStorage.getItem(STORE_KEYS.transpose) || '0'),
    showNoteNames: localStorage.getItem(STORE_KEYS.showNoteNames) !== 'false',
    hitDetection:  localStorage.getItem(STORE_KEYS.hitDetection) === 'true',
};

function _saveCfg(key, val) {
    _cfg[key] = val;
    const storeKey = STORE_KEYS[key];
    if (!storeKey) return;
    try { localStorage.setItem(storeKey, String(val)); } catch (_) {}
}

